    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        IngressoCinema ingresso = new IngressoCinema();
        String dia = campoDiaSemana.getText().toLowerCase();
        String estudan = campoEstudante.getText().toLowerCase();
        
        
            
        if (dia.equals("segunda")) {
            ingresso.setDiaSemana(DiaSemana.SEGUNDA);
        } else if (dia.equals("terca")) {
            ingresso.setDiaSemana(DiaSemana.TERCA);
        } else if (dia.equals("quarta")) {
            ingresso.setDiaSemana(DiaSemana.QUARTA);
        } else if (dia.equals("quinta")) {
            ingresso.setDiaSemana(DiaSemana.QUINTA);
        } else if (dia.equals("sexta")) {
            ingresso.setDiaSemana(DiaSemana.SEXTA);
        } else if (dia.equals("sabado")) {
            ingresso.setDiaSemana(DiaSemana.SABADO);
        } else if (dia.equals("domingo")) {
            ingresso.setDiaSemana(DiaSemana.DOMINGO);
        }
        
        if (estudan.equals("sim")) {
            ingresso.setEstudante(true);
        } else if (estudan.equals("nao")) {
            ingresso.setEstudante(false);
        }
        
        double precoFinal = ingresso.precoIngresso();
        
        campoPrecoFinal.setText("Pre√ßo: R$" + precoFinal);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

